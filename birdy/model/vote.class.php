class vote extends basemodel
{
	//------------------------------------------------------------------------------
	// * Cancel vote
	// Annule un vote en mettant l'id utilisateur et l'id du message à 0.
	//------------------------------------------------------------------------------
	public function cancel()
	{
		$this->utilisateur = $this->message = 0;
		$this->save();
	}
}

